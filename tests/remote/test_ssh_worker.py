"""Tests for SSH worker protocol and startup command generation.

Phase 0: Write tests BEFORE refactoring base64 out of SSH commands.

- TestWorkerScript: exercises _WORKER_SCRIPT directly via subprocess
- TestSSHWorkerStartup: checks the SSH command generated by SSHWorker.start()
- TestSSHWorkerExecute: checks the JSON request sent by SSHWorker.execute()
"""

from __future__ import annotations

import json
import subprocess
from unittest.mock import AsyncMock, MagicMock, patch

import pytest

from imas_codex.remote.ssh_worker import _WORKER_SCRIPT, SSHWorker

# ============================================================================
# Helpers
# ============================================================================


def _run_worker_with_requests(requests: list[dict]) -> list[dict]:
    """Run _WORKER_SCRIPT with a sequence of JSON requests.

    Returns all parsed JSON lines (including the startup ``{"ready": true}``
    line as the first element).
    """
    input_lines = "\n".join(json.dumps(r) for r in requests) + "\n"
    result = subprocess.run(
        ["python3", "-c", _WORKER_SCRIPT],
        input=input_lines,
        capture_output=True,
        text=True,
        timeout=10,
    )
    responses = []
    for line in result.stdout.strip().split("\n"):
        if line.strip():
            responses.append(json.loads(line))
    return responses


# ============================================================================
# TestWorkerScript — _WORKER_SCRIPT behaviour tested directly via subprocess
# ============================================================================


class TestWorkerScript:
    """Test the _WORKER_SCRIPT Python code directly by exec'ing it in a subprocess."""

    def test_exec_runs_script_and_returns_stdout(self):
        """A script that prints should produce ok=True with the printed output."""
        responses = _run_worker_with_requests(
            [{"action": "exec", "script": "print('hello')", "stdin": "{}"}]
        )
        # responses[0] is {"ready": True, "pid": N}
        assert responses[0].get("ready") is True
        resp = responses[1]
        assert resp["ok"] is True
        assert resp["stdout"] == "hello\n"

    def test_exec_error_returns_traceback(self):
        """A script that raises should produce ok=False with error and tb fields."""
        responses = _run_worker_with_requests(
            [{"action": "exec", "script": "raise ValueError('oops')", "stdin": "{}"}]
        )
        resp = responses[1]
        assert resp["ok"] is False
        assert "oops" in resp["error"]
        assert "Traceback" in resp.get("tb", "")

    def test_ping_returns_pong(self):
        """A ping request should produce ok=True with pong=True."""
        responses = _run_worker_with_requests([{"action": "ping"}])
        resp = responses[1]
        assert resp["ok"] is True
        assert resp["pong"] is True

    def test_eof_exits_cleanly(self):
        """Closing stdin (empty input) should cause the worker to exit with code 0."""
        result = subprocess.run(
            ["python3", "-c", _WORKER_SCRIPT],
            input="",
            capture_output=True,
            text=True,
            timeout=10,
        )
        assert result.returncode == 0

    def test_unknown_action_returns_error(self):
        """An unrecognised action should produce an error response."""
        responses = _run_worker_with_requests([{"action": "frobnicate"}])
        resp = responses[1]
        assert resp["ok"] is False
        assert "unknown action" in resp["error"].lower()

    def test_invalid_json_returns_error(self):
        """Sending garbage JSON should produce an error response (not crash)."""
        result = subprocess.run(
            ["python3", "-c", _WORKER_SCRIPT],
            input="this is not valid json\n",
            capture_output=True,
            text=True,
            timeout=10,
        )
        lines = [line for line in result.stdout.strip().split("\n") if line.strip()]
        # At least two lines: startup signal + error response
        assert len(lines) >= 2
        # The second line should report an error (not a successful parse)
        error_resp = json.loads(lines[1])
        assert error_resp["ok"] is False
        assert "invalid json" in error_resp["error"].lower()


