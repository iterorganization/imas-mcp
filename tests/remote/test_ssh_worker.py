class TestSSHWorkerStartup:
    """Test the startup command generated by SSHWorker.start()."""

    async def test_startup_command_structure(self):
        """SSH command must include PATH setup and a python3 invocation."""
        with patch("asyncio.create_subprocess_exec") as mock_exec:
            mock_proc = AsyncMock()
            mock_proc.stdout = AsyncMock()
            mock_proc.stdout.readline = AsyncMock(
                return_value=json.dumps({"ready": True, "pid": 1234}).encode() + b"\n"
            )
            mock_proc.returncode = None
            mock_exec.return_value = mock_proc

            worker = SSHWorker("test-host", 0)
            await worker.start(timeout=5)

            call_args = mock_exec.call_args
            # create_subprocess_exec receives *cmd unpacked — inspect positional args
            cmd_parts = list(call_args.args)
            full_cmd = " ".join(str(p) for p in cmd_parts)

            assert "ssh" in cmd_parts[0]
            assert "test-host" in full_cmd
            assert "python3" in full_cmd
            assert "PATH" in full_cmd

    @pytest.mark.xfail(reason="base64 elimination pending - security fix", strict=False)
    async def test_no_base64_in_startup_command(self):
        """Startup command must not contain 'base64' (XDR malware trigger)."""
        with patch("asyncio.create_subprocess_exec") as mock_exec:
            mock_proc = AsyncMock()
            mock_proc.stdout = AsyncMock()
            mock_proc.stdout.readline = AsyncMock(
                return_value=json.dumps({"ready": True, "pid": 1234}).encode() + b"\n"
            )
            mock_proc.returncode = None
            mock_exec.return_value = mock_proc

            worker = SSHWorker("test-host", 0)
            await worker.start(timeout=5)

            call_args = mock_exec.call_args
            cmd_parts = list(call_args.args)
            full_cmd = " ".join(str(p) for p in cmd_parts)

            assert "base64" not in full_cmd, (
                "Startup command contains 'base64' — this triggers XDR malware "
                "detection and must be eliminated."
            )

    async def test_nice_level_applied_to_startup(self):
        """When a nice level is configured for the host, 'nice -n N' must appear."""
        from imas_codex.remote.executor import _host_nice_levels

        # Register a nice level for our test host
        _host_nice_levels["nice-host"] = 10
        try:
            with patch("asyncio.create_subprocess_exec") as mock_exec:
                mock_proc = AsyncMock()
                mock_proc.stdout = AsyncMock()
                mock_proc.stdout.readline = AsyncMock(
                    return_value=json.dumps({"ready": True, "pid": 5678}).encode()
                    + b"\n"
                )
                mock_proc.returncode = None
                mock_exec.return_value = mock_proc

                worker = SSHWorker("nice-host", 0)
                await worker.start(timeout=5)

                call_args = mock_exec.call_args
                cmd_parts = list(call_args.args)
                full_cmd = " ".join(str(p) for p in cmd_parts)

                assert "nice -n 10" in full_cmd
        finally:
            del _host_nice_levels["nice-host"]


