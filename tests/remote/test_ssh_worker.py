# TestSSHWorkerExecute — JSON request protocol via SSHWorker.execute()
# ============================================================================


class TestSSHWorkerExecute:
    """Test the execute() request/response protocol."""

    def _make_alive_worker(self) -> SSHWorker:
        """Return a pre-wired SSHWorker that appears alive."""
        worker = SSHWorker("test-host", 0)
        worker._ready = True

        mock_proc = AsyncMock()
        mock_proc.returncode = None
        # stdin.write is synchronous in asyncio — use MagicMock
        mock_proc.stdin = MagicMock()
        mock_proc.stdin.write = MagicMock()
        mock_proc.stdin.drain = AsyncMock()
        worker._proc = mock_proc

        return worker

    async def test_execute_sends_script(self):
        """execute() must write a JSON line to the worker's stdin."""
        worker = self._make_alive_worker()

        ok_resp = json.dumps({"ok": True, "stdout": "done\n"}).encode() + b"\n"
        worker._proc.stdout = AsyncMock()
        worker._proc.stdout.readline = AsyncMock(return_value=ok_resp)

        await worker.execute("print('done')")

        worker._proc.stdin.write.assert_called_once()
        raw = worker._proc.stdin.write.call_args.args[0]
        payload = json.loads(raw.decode().strip())
        assert payload["action"] == "exec"
        assert "script" in payload

    async def test_execute_returns_stdout(self):
        """execute() must return the stripped stdout from the JSON response."""
        worker = self._make_alive_worker()

        ok_resp = json.dumps({"ok": True, "stdout": "hello\n"}).encode() + b"\n"
        worker._proc.stdout = AsyncMock()
        worker._proc.stdout.readline = AsyncMock(return_value=ok_resp)

        result = await worker.execute("print('hello')")
        assert result == "hello"

    async def test_execute_handles_error(self):
        """execute() must raise RuntimeError when ok=False."""
        worker = self._make_alive_worker()

        err_resp = (
            json.dumps(
                {"ok": False, "error": "NameError: x", "tb": "Traceback..."}
            ).encode()
            + b"\n"
        )
        worker._proc.stdout = AsyncMock()
        worker._proc.stdout.readline = AsyncMock(return_value=err_resp)

        with pytest.raises(RuntimeError, match="NameError"):
            await worker.execute("print(x)")

    @pytest.mark.xfail(reason="base64 elimination pending - security fix", strict=False)
    async def test_no_base64_in_exec_request(self):
        """exec request payload must not contain base64-encoded script."""
        worker = self._make_alive_worker()

        ok_resp = json.dumps({"ok": True, "stdout": ""}).encode() + b"\n"
        worker._proc.stdout = AsyncMock()
        worker._proc.stdout.readline = AsyncMock(return_value=ok_resp)

        await worker.execute("x = 1")

        raw = worker._proc.stdin.write.call_args.args[0]
        payload = json.loads(raw.decode().strip())

        # After refactoring, script should be sent as plain text, not base64
        script_field = payload.get("script", "")
        assert script_field == "x = 1", (
            f"Script is base64-encoded in the request payload: {script_field!r}. "
            "This triggers XDR malware detection and must be eliminated."
        )
