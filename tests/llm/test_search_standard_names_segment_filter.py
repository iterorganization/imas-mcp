# Test: end-to-end single-filter — single segment filter returns only matches
# ---------------------------------------------------------------------------


class TestSegmentFilterEndToEnd:
    """Integration-style tests over _search_standard_names with mock graph."""

    def test_single_segment_filter_returns_matching_rows(self) -> None:
        """physical_base='temperature' should return temperature-based names only."""
        matching_row = _sn_row("electron_temperature")
        gc = _make_gc()

        with patch(
            "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
            return_value=[matching_row],
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            result = _search_standard_names(
                "temperature", physical_base="temperature", gc=gc
            )

        assert "electron_temperature" in result
        assert "plasma_current" not in result

    def test_two_segment_and_filter(self) -> None:
        """physical_base + subject must both appear in the segment_filters dict."""
        gc = _make_gc(return_value=[])

        captured: list[dict] = []

        def fake_segment_search(gc_, query, k, segment_filters):
            captured.append(segment_filters)
            return []

        with patch(
            "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
            side_effect=fake_segment_search,
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            _search_standard_names(
                "electron temperature",
                physical_base="temperature",
                subject="electron",
                gc=gc,
            )

        assert len(captured) == 1
        filters = captured[0]
        assert filters.get("physical_base") == "temperature"
        assert filters.get("subject") == "electron"

    def test_no_match_returns_empty_report(self) -> None:
        """When graph returns no rows, result must indicate no matches found."""
        gc = _make_gc()

        with patch(
            "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
            return_value=[],
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            result = _search_standard_names(
                "nothing", physical_base="nonexistent_token", gc=gc
            )

        assert "No standard names found" in result

    def test_fallback_when_no_typed_edges_in_graph(self) -> None:
        """When graph has no typed edges, segment filter returns empty — no error."""
        # Graph query returns nothing (typed edges not yet migrated)
        gc = _make_gc(return_value=[])

        with patch(
            "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
            return_value=[],
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            result = _search_standard_names(
                "temperature", physical_base="temperature", gc=gc
            )

        # Must not raise; must return a well-formed "no results" report
        assert isinstance(result, str)
        assert "No standard names found" in result


