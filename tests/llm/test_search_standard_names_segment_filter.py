"""Tests for grammar-segment filters in _search_standard_names (W40 Phase 5).

Verifies that when physical_base / subject / transformation / … kwargs are
provided, the search routes to the segment-filter Cypher path using typed
graph edges (HAS_PHYSICAL_BASE, HAS_SUBJECT, …).

All tests are mock-based — no live Neo4j required.
"""

from __future__ import annotations

from unittest.mock import MagicMock, call, patch

import pytest

# ---------------------------------------------------------------------------
# Helpers / fixtures
# ---------------------------------------------------------------------------


def _make_gc(return_value=None):
    """Create a mock GraphClient."""
    gc = MagicMock()
    gc.query = MagicMock(return_value=return_value or [])
    return gc


def _sn_row(name: str = "electron_temperature") -> dict:
    """Minimal StandardName row dict."""
    return {
        "name": name,
        "description": f"Description of {name}",
        "kind": "scalar",
        "unit": "eV",
        "pipeline_status": "drafted",
        "documentation": "",
        "cocos_transformation_type": "",
        "cocos": None,
        "score": 1.0,
    }


# ---------------------------------------------------------------------------
# Test: segment filter routes to _segment_filter_search_standard_names
# ---------------------------------------------------------------------------


class TestSegmentFilterRouting:
    """When any segment filter kwarg is set, the search must use
    _segment_filter_search_standard_names, not the vector/keyword path."""

    def test_physical_base_filter_calls_segment_search(self) -> None:
        """physical_base kwarg must route to _segment_filter_search_standard_names."""
        gc = _make_gc(return_value=[_sn_row("electron_temperature")])

        with (
            patch(
                "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
                return_value=[_sn_row("electron_temperature")],
            ) as mock_seg,
            patch("imas_codex.llm.sn_tools._vector_search_standard_names") as mock_vec,
            patch("imas_codex.llm.sn_tools._keyword_search_standard_names") as mock_kw,
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            result = _search_standard_names(
                "temperature", physical_base="temperature", gc=gc
            )

        mock_seg.assert_called_once()
        mock_vec.assert_not_called()
        mock_kw.assert_not_called()
        assert "electron_temperature" in result

    def test_subject_filter_calls_segment_search(self) -> None:
        """subject kwarg must route to _segment_filter_search_standard_names."""
        gc = _make_gc(return_value=[])

        with patch(
            "imas_codex.llm.sn_tools._segment_filter_search_standard_names",
            return_value=[],
        ) as mock_seg:
            from imas_codex.llm.sn_tools import _search_standard_names

            _search_standard_names("electron", subject="electron", gc=gc)

        mock_seg.assert_called_once()

    def test_no_filter_uses_keyword_fallback(self) -> None:
        """With no segment filters and no embedding, keyword search is used."""
        gc = _make_gc(return_value=[])

        with (
            patch(
                "imas_codex.llm.sn_tools._keyword_search_standard_names",
                return_value=[],
            ) as mock_kw,
            patch(
                "imas_codex.llm.sn_tools._segment_filter_search_standard_names"
            ) as mock_seg,
        ):
            from imas_codex.llm.sn_tools import _search_standard_names

            _search_standard_names("temperature", gc=gc)

        mock_kw.assert_called_once()
        mock_seg.assert_not_called()


# ---------------------------------------------------------------------------
# Test: _segment_filter_search_standard_names builds correct Cypher
# ---------------------------------------------------------------------------


class TestSegmentFilterCypher:
    """Verify the Cypher generated by _segment_filter_search_standard_names."""

    def test_single_filter_cypher_contains_edge_label(self) -> None:
        """physical_base filter → Cypher must reference HAS_PHYSICAL_BASE."""
        gc = _make_gc()
        from imas_codex.llm.sn_tools import _segment_filter_search_standard_names

        _segment_filter_search_standard_names(
            gc, "temperature", 20, {"physical_base": "temperature"}
        )

        assert gc.query.called
        cypher = gc.query.call_args[0][0]
        assert "HAS_PHYSICAL_BASE" in cypher

    def test_two_filter_cypher_contains_both_labels(self) -> None:
        """physical_base + subject → both edge labels in Cypher."""
        gc = _make_gc()
        from imas_codex.llm.sn_tools import _segment_filter_search_standard_names

        _segment_filter_search_standard_names(
            gc,
            "electron temperature",
            20,
            {"physical_base": "temperature", "subject": "electron"},
        )

        cypher = gc.query.call_args[0][0]
        assert "HAS_PHYSICAL_BASE" in cypher
        assert "HAS_SUBJECT" in cypher

    def test_filter_param_value_passed_to_query(self) -> None:
        """Token value must be passed as a Cypher parameter, not inlined."""
        gc = _make_gc()
        from imas_codex.llm.sn_tools import _segment_filter_search_standard_names

        _segment_filter_search_standard_names(
            gc, "temperature", 10, {"physical_base": "temperature"}
        )

        kwargs = gc.query.call_args[1]
        # One of the kwargs should be the token value
        assert "temperature" in kwargs.values()

    def test_unknown_segment_skipped_gracefully(self) -> None:
        """An unrecognised segment key must not raise and not appear in Cypher."""
        gc = _make_gc()
        from imas_codex.llm.sn_tools import _segment_filter_search_standard_names

        # Should not raise
        _segment_filter_search_standard_names(
            gc, "anything", 5, {"not_a_real_segment": "foo"}
        )

        assert gc.query.called
        cypher = gc.query.call_args[0][0]
        # Only StandardName match should be present
        assert "MATCH (sn:StandardName)" in cypher

    def test_k_limit_passed_to_query(self) -> None:
        """k parameter must be forwarded as the LIMIT value."""
        gc = _make_gc()
        from imas_codex.llm.sn_tools import _segment_filter_search_standard_names

        _segment_filter_search_standard_names(gc, "x", 7, {"subject": "electron"})

        kwargs = gc.query.call_args[1]
        assert kwargs.get("k") == 7


